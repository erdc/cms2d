   subroutine tide_fes(tdbname, tdbpath, npts, xpts, ypts, ntcin, namein, ntc, name, etamp, etpha)
! Extracts the Tidal Constituent variables from a LeProvost database
! written by Alex Sanchez, USACE-CHL
!******************************************************************************
      use prec_def
      use diag_lib, only: diag_print_error
      implicit none

      !--- Input/Output -------------
      integer, intent(in)      :: npts
      real(ikind), intent(in)      :: xpts(npts), ypts(npts)
      integer, intent(in)      :: ntcin
      integer, intent(inout)   :: ntc
      character(len=*), intent(in) :: tdbname
      character(len=*), intent(in) :: tdbpath
      character(len=*), intent(inout), pointer :: namein(:)
      character(len=10), intent(out), pointer  :: name(:)
      real(ikind), intent(out), pointer        :: etamp(:, :), etpha(:, :)
      !---- Internal Variables ----------
      integer :: k

      if (ntcin == 0) then !None specified, use all.
         if (tdbname(1:5) == 'FES95') then
            ntc = 8
            allocate (name(ntc))
            name(1) = 'M2'
            name(2) = 'S2'
            name(3) = 'N2'
            name(4) = 'K1'
            name(5) = 'O1'
            name(6) = '2N2'
            name(7) = 'Q1'
            name(8) = 'K2'

         elseif (tdbname(1:7) == 'FES2004') then
            ntc = 13
            allocate (name(ntc))
            name(1) = 'M2'
            name(2) = 'S2'
            name(3) = 'N2'
            name(4) = 'K1'
            name(5) = 'M4'
            name(6) = 'O1'
            name(7) = '2N2'
            name(8) = 'S1'
            name(9) = 'MM'
            name(10) = 'MF'
            name(11) = 'Q1'
            name(12) = 'P1'
            name(13) = 'K2'

         elseif (tdbname(1:9) == 'LEPROVOST') then
            ntc = 13
            allocate (name(ntc))
            name(1) = 'M2'
            name(2) = 'S2'
            name(3) = 'N2'
            name(4) = 'K1'
            name(5) = 'O1'
            name(6) = 'NU2'
            name(7) = 'MU2'
            name(8) = '2N2'
            name(9) = 'Q1'
            name(10) = 'T2'
            name(11) = 'P1'
            name(12) = 'L2'
            name(13) = 'K2'
         else
            call diag_print_error('Invalid Tidal Database Name ')
         end if
      else
         ntc = ntcin
         allocate (name(ntc))
         do k = 1, ntc
            name(k) = namein(k)
         end do
      end if

      allocate (etamp(npts, ntc), etpha(npts, ntc))
      call FES_interp(tdbpath, tdbname, npts, xpts, ypts, ntc, name, etamp, etpha)

      return
   end subroutine tide_fes

!**********************************************************************
   subroutine FES_interp(fespath, fesname, npts, xlon, ylat, ntc, tcname, etamp, etpha)
!  Program to extract the FES95.2 tidal database of Le Provost to     *
!  scattered points                                                   *
!                                                                     *
!  Points located inside fully active FES grid square are assigned    *
!  values using bilinear interpolation. Points located inside a FES   *
!  grid square with only 3 active corners are assigned values using   *
!  linear interpolation from the 3 active corners.  Points located    *
!  inside a FES grid square with only 2 or 1 active nodes are not     *
!  computed.  Rather the dummy value of -999, -999. is assigned.      *
!                                                                     *
!           modified by Mitch Brown for use in CMS 08/17/23           *
!           modified by Alex Sanchez for use in CMS 08/13/12          *
!            Comment line cleanup by R.L. 10/12/01                    *
!             Written by R.L.  3/10/1995    v2.06                     *
!               Modified from OSU_tea.for  v1.03                      *
!                 and from French_tea.for v1.05                       *
!                                                                     *
!  The FES95.2 tidal database has separate input files for each       *
!  constituent.  Each file is ascii with the format:                  *
!  lonmin, lonmax     (-180, 180)                                     *
!  latmin, latmax     ( -90,  90)                                     *
!  dlon,dlat          ( 0.5, 0.5)                                     *
!  nlon,nlat          ( 720, 361)                                     *
!  UNDEFa,UNDEFp     (999.9,999.9)                                    *
!                                                                     *
!  do j=1,nlat                                                        *
!     do i=1,nlon-1,30                                                *
!        (Ga(ii,j),ii=i,i+29)                                         *
!        (Gp(ii,j),ii=i,i+29)                                         *
!     enddo                                                           *
!  enddo                                                              *
!**********************************************************************
      use diag_def, only: debug_mode
      use diag_lib, only: diag_print_error
      use prec_def
      implicit none

      !PARAMETERS
      real(ikind), parameter :: deg2rad = atan(1.0)/45.0
      real(ikind), parameter :: rad2deg = 45.0/atan(1.0)
      !INPUT/OUTPUT
      integer :: npts, ntc
      real(ikind), intent(in) :: xlon(npts), ylat(npts)
      real(ikind), intent(out):: etamp(npts, ntc), etpha(npts, ntc)
      character(len=*), intent(in) :: tcname(ntc) !Tidal constituent name
      character(len=*), intent(in) :: fesname     !Tidal database name
      character(len=*), intent(in) :: fespath     !Tidal database path
      !INTERNAL VARIABLES
      integer :: i, ii, j, k, n, nlon, nlat
      real(ikind), allocatable :: Ga(:, :), Gp(:, :)
      integer       :: ixlo, ixhi, jylo, jyhi
      real(ikind)   :: phi(4), c(4), s(4), tc, ts, dlon, dlat
      real(ikind)   :: latmin, latmax, lonmin, lonmax, xlonlo, ylatlo
      real(ikind)   :: xmin, xmax, ymin, ymax
      integer       :: imin, imax, jmin, jmax
      real(ikind)   :: UNDEFa, UNDEFp, UNDEF, zeta, eta, amp, pha
      character*200 :: datafile
      character*55  :: ampfile, phafile, suffix
      character*3   :: consname
      logical       :: found

      !Calculate domain of input points
      xmax = maxval(xlon)
      xmin = minval(xlon)
      ymax = maxval(ylat)
      ymin = minval(ylat)

      write (*, *) 'Reading in Tidal Database File(s) '
      if (fesname(1:5) == 'FES95') then
         suffix = '.fes95.2'
      elseif (fesname(1:7) == 'FES2004') then
         suffix = '_fes2004.asc'
      else
         suffix = '.legi'
      end if

      do k = 1, ntc
         consname = tcname(k)
         datafile = trim(fespath)//trim(consname)//trim(suffix)
         inquire (file=datafile, exist=found)
         if (.not. found) then
            call diag_print_error('Could not find tidal database file: '//trim(datafile))
         end if
         !write(*,*) '  ',trim(datafile)
         open (11, file=datafile)
         read (11, *) lonmin, lonmax
         read (11, *) latmin, latmax
         read (11, *) dlon, dlat
         read (11, *) nlon, nlat
         read (11, *) UNDEFa, UNDEFp
         if (abs(UNDEFa - UNDEFp) > 1.0e-5) then
            call diag_print_error('UNDEFa <> UNDEFp ')
         else
            UNDEF = UNDEFa
         end if
         if (fesname(1:7) == 'FES2004') then
            lonmin = lonmin - 360.0
            lonmax = lonmax - 360.0
         end if
         if (allocated(Ga)) deallocate (Ga, Gp)
         allocate (Ga(nlon, nlat), Gp(nlon, nlat))
         do j = 1, nlat
            do i = 1, nlon - 1, 30
               read (11, *) (Ga(ii, j), ii=i, i + 29)
               read (11, *) (Gp(ii, j), ii=i, i + 29)
            end do
            if (fesname(1:7) == 'FES2004') then !Reading of redundant value
               read (11, *)
               read (11, *)
            end if
         end do
         close (11)

         if (debug_mode) then
            ampfile = trim(fesname)//'_'//trim(consname)//'_Amp.xyz'
            phafile = trim(fesname)//'_'//trim(consname)//'_Pha.xyz'
            open (767, file=ampfile)
            open (878, file=phafile)
            jmin = (ymin - latmin)/dlat + 1
            jmax = (ymax - latmin)/dlat + 2
            imin = (xmin - lonmin)/dlon + 1
            imax = (xmax - lonmin)/dlon + 2
            imin = max(imin - 100, 1)
            imax = min(imax + 100, nlon)
            jmin = max(jmin - 100, 1)
            jmax = min(jmax + 100, nlat)
            do j = jmin, jmax
               ylatlo = latmin + (j - 1)*dlat
               do i = imin, imax
                  xlonlo = lonmin + (i - 1)*dlon
                  if (abs(Ga(i, j) - UNDEFa) > 1.0e-4) then
                     write (767, '(3(F9.3))') xlonlo, ylatlo, Ga(i, j)
                  end if
                  if (abs(Gp(i, j) - UNDEFp) > 1.0e-4) then
                     write (878, '(3(F9.3))') xlonlo, ylatlo, Gp(i, j)
                  end if
               end do
            end do
            close (767)
            close (878)
         end if

         !Main loop for every point
         do n = 1, npts
            !Find where in the FES grid the node is
            ixlo = (xlon(n) - lonmin)/dlon + 1
            xlonlo = lonmin + (ixlo - 1)*dlon
            ixhi = ixlo + 1
            if (ixlo == nlon) ixhi = 1
            jylo = (ylat(n) - latmin)/dlat + 1
            ylatlo = latmin + (jylo - 1)*dlat
            jyhi = jylo + 1

            zeta = (xlon(n) - xlonlo)/dlon
            eta = (ylat(n) - ylatlo)/dlat
            if ((Ga(ixlo, jylo) == UNDEF) .or. (Gp(ixlo, jylo) == UNDEF)) then
               phi(1) = 0.0
            else
               phi(1) = (1.0 - zeta)*(1.0 - eta)
               c(1) = Ga(ixlo, jylo)*cos(deg2rad*Gp(ixlo, jylo))
               s(1) = Ga(ixlo, jylo)*sin(deg2rad*Gp(ixlo, jylo))
            end if
            if ((Ga(ixhi, jylo) == UNDEF) .or. (Gp(ixhi, jylo) == UNDEF)) then
               phi(2) = 0.0
            else
               phi(2) = zeta*(1.0 - eta)
               c(2) = Ga(ixhi, jylo)*cos(deg2rad*Gp(ixhi, jylo))
               s(2) = Ga(ixhi, jylo)*sin(deg2rad*Gp(ixhi, jylo))
            end if
            if ((Ga(ixhi, jyhi) == UNDEF) .or. (Gp(ixhi, jyhi) == UNDEF)) then
               phi(3) = 0.0
            else
               phi(3) = zeta*eta
               c(3) = Ga(ixhi, jyhi)*cos(deg2rad*Gp(ixhi, jyhi))
               s(3) = Ga(ixhi, jyhi)*sin(deg2rad*Gp(ixhi, jyhi))
            end if
            if ((Ga(ixlo, jyhi) == UNDEF) .or. (Gp(ixlo, jyhi) == UNDEF)) then
               phi(4) = 0.0
            else
               phi(4) = (1.0 - zeta)*eta
               c(4) = Ga(ixlo, jyhi)*cos(deg2rad*Gp(ixlo, jyhi))
               s(4) = Ga(ixlo, jyhi)*sin(deg2rad*Gp(ixlo, jyhi))
            end if
            phi = phi/max(sum(phi), 1.0e-10)

            !Interpolate constituent
            tc = 0.0; ts = 0.0
            do i = 1, 4
               tc = tc + phi(i)*c(i)
               ts = ts + phi(i)*s(i)
            end do

            !Compute amplitude and phase
            amp = sqrt(tc*tc + ts*ts)
            if (amp > 1.e-6) then
               pha = rad2deg*acos(tc/amp)
            else
               pha = 0.0
            end if
            amp = amp/100.0 !Convert from cm to m
            if (ts < 0) pha = 360.0 - pha
            etamp(n, k) = amp
            etpha(n, k) = pha
         end do !n
      end do !k

      return
   end subroutine FES_interp

!******************************************************************************
   subroutine tdb_interp_tri(npts, intp, cntp, nns, ntc, eamp, epha, etamp, etpha, &
                             uamp, upha, vamp, vpha, utamp, utpha, vtamp, vtpha)
! Spatial interpolation of tidal database constituents.
! Input and output phases are in degrees
!
! writen by Alex Sanchez, USACE-CHL
!******************************************************************************
      use prec_def
      implicit none
      !Input/Output
      integer, intent(in) :: npts, nns, ntc
      integer, intent(in) :: intp(0:3, npts)
      real(ikind), intent(in) :: cntp(3, npts)
      real(ikind), intent(in) :: eamp(nns, ntc), epha(nns, ntc) !tdb values
      real(ikind), intent(out) :: etamp(npts, ntc), etpha(npts, ntc) !interpolated values
      real(ikind), intent(in), optional :: uamp(nns, ntc), upha(nns, ntc) !tdb values
      real(ikind), intent(in), optional :: vamp(nns, ntc), vpha(nns, ntc) !tdb values
      real(ikind), intent(inout), optional :: utamp(npts, ntc), utpha(npts, ntc) !interpolated values
      real(ikind), intent(inout), optional :: vtamp(npts, ntc), vtpha(npts, ntc) !interpolated values
      !Internal variables
      integer :: i, j, k, kk
      real(ikind) :: etr, eti, utr, uti, vtr, vti
      real(ikind), parameter :: deg2rad = atan(1.0)/45.0
      real(ikind), parameter :: rad2deg = 45.0/atan(1.0)

      do i = 1, npts
         do j = 1, ntc
            etr = 0.0; eti = 0.0
            do kk = 1, intp(0, i)
               k = intp(kk, i)
               etr = etr + cntp(kk, i)*eamp(k, j)*cos(epha(k, j)*deg2rad)
               eti = eti + cntp(kk, i)*eamp(k, j)*sin(epha(k, j)*deg2rad)
            end do
            etamp(i, j) = sqrt(etr*etr + eti*eti)
            if (etamp(i, j) == 0.0) then
               etpha(i, j) = 0.0
            else
               etpha(i, j) = acos(etr/etamp(i, j))*rad2deg
               if (eti < 0.0) etpha(i, j) = 360.0 - etpha(i, j)
            end if
         end do
      end do

      if (.not. present(vtpha)) return

      do i = 1, npts
         do j = 1, ntc
            utr = 0.0; uti = 0.0
            vtr = 0.0; vti = 0.0
            do kk = 1, intp(0, i)
               k = intp(kk, i)
               utr = utr + cntp(kk, i)*uamp(k, j)*cos(upha(k, j)*deg2rad)
               uti = uti + cntp(kk, i)*uamp(k, j)*sin(upha(k, j)*deg2rad)
               vtr = vtr + cntp(kk, i)*vamp(k, j)*cos(vpha(k, j)*deg2rad)
               vti = vti + cntp(kk, i)*vamp(k, j)*sin(vpha(k, j)*deg2rad)
            end do
            utamp(i, j) = sqrt(utr*utr + uti*uti)
            vtamp(i, j) = sqrt(vtr*vtr + vti*vti)
            if (utamp(i, j) == 0.0) then
               utpha(i, j) = 0.0
            else
               utpha(i, j) = acos(utr/utamp(i, j))*rad2deg
               if (uti < 0.0) utpha(i, j) = 360.0 - utpha(i, j)
            end if
            if (vtamp(i, j) == 0.0) then
               vtpha(i, j) = 0.0
            else
               vtpha(i, j) = acos(vtr/vtamp(i, j))*rad2deg
               if (vti < 0.0) vtpha(i, j) = 360.0 - vtpha(i, j)
            end if
         end do
      end do

      return
   end subroutine tdb_interp_tri

