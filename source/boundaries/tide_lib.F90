        name(5) = 'O1'
        name(6) = '2N2'
        name(7) = 'Q1'
        name(8) = 'K2'
        
      elseif(tdbname(1:7)=='FES2004')then
        ntc = 13
        allocate(name(ntc))        
        name(1) = 'M2'
        name(2) = 'S2'
        name(3) = 'N2'
        name(4) = 'K1'
        name(5) = 'M4'
        name(6) = 'O1'        
        name(7) = '2N2'
        name(8) = 'S1'
        name(9) = 'MM'
        name(10) = 'MF'
        name(11) = 'Q1'
        name(12) = 'P1'
        name(13) = 'K2'
   
